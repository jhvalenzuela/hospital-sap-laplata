            <p>Acto formal de retorno de un paciente al establecimiento de origen (que lo ha referido) tras resolución de la causa responsable por la referencia, y siempre acompañado de las informaciones necesarias al seguimiento del mismo, en el establecimiento de origen</p>

            <h5>Copago</h5>
