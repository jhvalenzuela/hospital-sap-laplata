            <p>Consultas con médicos especialistas (ginecólogo, pediatra, cirujano, etc.).</p>

            <h5>Atención inicial de urgencias</h5>
            <p>Son aquellas intervenciones realizadas a una persona con patología de urgencia y que tiendan a estabilizarla en sus signos vitales, realizar un diagnóstico de impresión y definirle el destino inmediato, tomando como base el nivel de atención y el grado de complejidad de la entidad que realiza la atención inicial de urgencia.</p>

            <h5>Autorización</h5>
            <p>Acto administrativo por el cual la autoridad competente emite un documento permitiendo al solicitante ejecutar una práctica o cualquier acción especificada</p>

            <h5>Ayuda diagnóstica</h5>
            <p>Instrumento que permite esclarecer un diagnóstico clínico. Por ejemplo exámenes de laboratorio, endoscopias, rayos x etc.</p>
